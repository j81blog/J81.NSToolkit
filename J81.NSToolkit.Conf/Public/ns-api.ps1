function Invoke-NSAddApispec {
    <#
    .SYNOPSIS
        Add API Configuration config Object.
    .DESCRIPTION
        Configuration for API specification resource.
    .PARAMETER Name 
        Name for the spec. Must begin with an ASCII alphanumeric or underscore (_) character, and must contain only ASCII alphanumeric, underscore, hash (#), period (.), space, colon (:), at (@), equals (=), and hyphen (-) characters. Cannot be changed after the spec is created. 
    .PARAMETER File 
        Name of and, optionally, path to the api spec file. The spec file should be present on the appliance's hard-disk drive or solid-state drive. Storing a spec file in any location other than the default might cause inconsistency in a high availability setup. /nsconfig/apispec/ is the default path. 
    .PARAMETER Type 
        Input format of the spec file. The three formats supported by the appliance are: 
        PROTO 
        OAS/Swagger 
        GRAPHQL. 
         
        Possible values = PROTO, OAS, GRAPHQL 
    .PARAMETER PassThru 
        Return details about the created apispec item.
    .EXAMPLE
        PS C:\>Invoke-NSAddApispec -name <string> -file <string>
        An example how to add apispec config Object(s).
    .NOTES
        File Name : Invoke-NSAddApispec
        Version   : v2311.1622
        Author    : John Billekens
        Reference : https://developer-docs.netscaler.com/en-us/adc-nitro-api/current-release/configuration/api/apispec/
        Requires  : PowerShell v5.1 and up
                    NS 13.x and up.
                    NS 12 and lower may work, not guaranteed (best effort).
    .LINK
        https://blog.j81.nl
    #>
    [CmdletBinding(SupportsShouldProcess = $true, ConfirmImpact = "Low")]
    [System.Diagnostics.CodeAnalysis.SuppressMessageAttribute('PSAvoidUsingPlainTextForPassword', '')]
    [System.Diagnostics.CodeAnalysis.SuppressMessageAttribute('PSAvoidUsingUserNameAndPasswordParams', '')]
    [System.Diagnostics.CodeAnalysis.SuppressMessageAttribute('PSUseBOMForUnicodeEncodedFile', '')]
    param(
        [Parameter(DontShow)]
        [Object]$NSSession = (Get-NSSession),

        [Parameter(Mandatory)]
        [ValidateScript({ $_.Length -gt 1 })]
        [ValidatePattern('^(?>(?>[a-zA-Z0-9]|[_])+(?>[\x00-\x7F]|[_]|[#]|[.][ ]|[:]|[@]|[=]|[-])*)$')]
        [string]$Name,

        [Parameter(Mandatory)]
        [ValidateScript({ $_.Length -gt 1 })]
        [string]$File,

        [ValidateSet('PROTO', 'OAS', 'GRAPHQL')]
        [string]$Type = 'OAS',

        [Switch]$PassThru 
    )
    begin {
        Write-Verbose "Invoke-NSAddApispec: Starting"
    }
    process {
        try {
            $payload = @{ name = $name
                file           = $file
            }
            if ( $PSBoundParameters.ContainsKey('type') ) { $payload.Add('type', $type) }
            if ( $PSCmdlet.ShouldProcess("apispec", "Add API Configuration config Object") ) {
                $result = Invoke-NSNitroApi -NSSession $NSSession -Method POST -NitroPath nitro/v1/config -Type apispec -Payload $payload -GetWarning
                #HTTP Status Code on Success: 201 Created
                #HTTP Status Code on Failure: 4xx <string> (for general HTTP errors) or 5xx <string> (for NetScaler-specific errors). The response payload provides details of the error
